export function getMovesForLevel(speciesId: number, level: number): number[] {
  const learnset = MOVE_LEARNSETS[speciesId];
  if (!learnset) return [];
  
  const moves: number[] = [];
  for (const [lvl, moveIds] of Object.entries(learnset)) {
    if (parseInt(lvl) <= level) {
      moves.push(...moveIds);
    }
  }
  return [...new Set(moves)].slice(0, 4); // Max 4 moves, unique
}

export function getNewMovesAtLevel(speciesId: number, level: number): number[] {
  const learnset = MOVE_LEARNSETS[speciesId];
  if (!learnset) return [];
  
  return learnset[level] || [];
}
